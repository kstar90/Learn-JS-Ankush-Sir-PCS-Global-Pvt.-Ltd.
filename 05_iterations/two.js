// While Loop

let index = 0;

while (index <= 10) {
    console.log(`Value of index is: ${index}`);
    index = index + 2;
}

let myArray = ['flash', 'batman', 'superman', 'wonder woman'];

let arr = 0;
while ( arr < myArray.length ) {
    console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1;
}

